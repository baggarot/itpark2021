package lesson7;

public class Tiger {

    String name;
    String subspecies;
    double weight;
    double run;
    double swimming;

    public Tiger(String name, String subspecies, double weight, double run, double swimming) {
        this.name = name;
        this.subspecies = subspecies;
        this.weight = weight;
        this.run = run;
        this.swimming = swimming;
    }
}
